// countOnly function
const countOnly = function(allItems, itemsToCount) {
  const results = {};

  for (const item of allItems) {
    // Increment the counter for each item if it exists in itemsToCount
    if (itemsToCount[item]) { 
      if (results[item]) {
        results[item] += 1;
      } else {
        results[item] = 1;
      }
    }
  }

  return results;
};

